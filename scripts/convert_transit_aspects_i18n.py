def convert():
    with open(SRC, "r", encoding="utf-8") as f:
        data = json.load(f)

    print(f"Loaded {len(data)} entries")

    # Detect if already in i18n format (from previous run)
    first_val = next(iter(data.values()))
    already_i18n = "en" in first_val and "ru" in first_val

    i18n_data = {}
    aspect_names = ["conjunction", "sextile", "square", "trine", "opposition"]

    for key in data:
        # Extract original English content
        if already_i18n:
            en_block = first_val_entry = data[key]["en"]
        else:
            en_block = data[key]

        # Parse key to get transit planet, aspect, natal point
        transit_planet = aspect = natal_point = None
        for asp in aspect_names:
            marker = f"_{asp}_"
            if marker in key:
                idx = key.index(marker)
                transit_planet = key[:idx]
                natal_point = key[idx + len(marker):]
                aspect = asp
                break

        if not transit_planet or transit_planet not in TRANSIT_THEMES:
            print(f"WARN: Could not parse key or unknown transit planet: {key}")
            i18n_data[key] = {"en": en_block, "ru": en_block}
            continue

        if natal_point not in NATAL_THEMES:
            print(f"WARN: Unknown natal point '{natal_point}' in key: {key}")
            i18n_data[key] = {"en": en_block, "ru": en_block}
            continue

        # Generate Russian
        ru_block = generate_ru_entry(transit_planet, aspect, natal_point)

        i18n_data[key] = {
            "en": {
                "meaning": en_block["meaning"],
                "action": en_block["action"],
                "keywords": en_block["keywords"],
            },
            "ru": ru_block,
        }

    # Write back
    with open(SRC, "w", encoding="utf-8") as f:
        json.dump(i18n_data, f, ensure_ascii=False, indent=2)

    print(f"Converted {len(i18n_data)} entries to i18n format")
    print(f"Written to {os.path.abspath(SRC)}")

    # Show samples
    samples = ["Sun_conjunction_Sun", "Moon_square_Venus", "Saturn_opposition_MC",
               "Pluto_trine_Moon", "Chiron_conjunction_ASC", "North Node_sextile_Jupiter",
               "Lilith_square_Pluto", "Neptune_opposition_Venus"]
    for k in samples:
        if k in i18n_data:
            print(f"\n{'='*60}")
            print(f"  {k}")
            print(f"{'='*60}")
            entry = i18n_data[k]
            print(f"  EN meaning: {entry['en']['meaning'][:100]}...")
            print(f"  EN action:  {entry['en']['action']}")
            print(f"  EN kw:      {entry['en']['keywords']}")
            print(f"  RU meaning: {entry['ru']['meaning'][:120]}...")
            print(f"  RU action:  {entry['ru']['action']}")
            print(f"  RU kw:      {entry['ru']['keywords']}")


if __name__ == "__main__":
    convert()
