    meanings = t_theme["meanings"][tone]
    meaning_idx = hash(f"{transit_planet}_{aspect}_{natal_point}_m") % len(meanings)
    base_meaning = meanings[meaning_idx]

    # Add natal point context
    natal_specific = n_theme["specifics"][tone]
    meaning = f"{base_meaning} {natal_specific} В зоне {n_theme['area']}."

